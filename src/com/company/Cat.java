package com.company;

public class Cat {
    private String name;
    private int age;
    private int weiht;
    private String paroda;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getWeiht() {
        return weiht;
    }

    public void setWeiht(int weiht) {
        this.weiht = weiht;
    }

    public String getParoda() {
        return paroda;
    }

    public void setParoda(String paroda) {
        this.paroda = paroda;
    }
}
