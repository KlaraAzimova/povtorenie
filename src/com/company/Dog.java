package com.company;

public class Dog {
    private String name;
    private int age;
    private String breed;
    private int weiht;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getBreed() {
        return breed;
    }

    public void setBreed(String breed) {
        this.breed = breed;
    }

    public int getWeiht() {
        return weiht;
    }

    public void setWeiht(int weiht) {
        this.weiht = weiht;
    }
}
